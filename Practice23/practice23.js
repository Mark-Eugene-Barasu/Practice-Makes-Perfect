function changeColor() {
    document.getElementById("theBody").style.backgroundColor="rgb(149, 248, 0)"
    document.getElementById("para").style.color="rgb(255, 0, 200)"
}

function unChangeColor() {
    document.getElementById("theBody").style.backgroundColor="rgb(255, 0, 200)"
    document.getElementById("para").style.color="rgb(149, 248, 0)"
}