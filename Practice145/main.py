print("String")
a = "Hello World"
print(f"type of {a} = {type(a)}")
print("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~")

print("Numbers")
b = 20
c = 20.5
d = 1j
print(f"type of {b} = {type(b)} \ntype of {c} = {type(c)} \ntype {d} = {type(d)}")
print("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~")

print("Sequence")
e = ["apple", "banana", "cherry"]
f = ("apple", "banana", "cherry")
g = range(6)
print(f"type of {e} = {type(e)} \ntype of {f} = {type(f)} \ntype {g} = {type(g)}")
print("~~~~~~~~~~~~~~~~~~~~~~~~~~~~~")
